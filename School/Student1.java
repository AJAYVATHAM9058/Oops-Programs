
public class Student1 {

	private String name;
	private int age;
	
	public Student1(String name,int age) {
		this.age = age;
		this.name = name;
	}
	public String getname() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public void setname(String name) {
		this.name = name;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
